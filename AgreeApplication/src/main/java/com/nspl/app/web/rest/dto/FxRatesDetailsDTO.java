package com.nspl.app.web.rest.dto;

import java.math.BigDecimal;
import java.time.ZonedDateTime;

public class FxRatesDetailsDTO {
	
	private Long id;
	private String fxRateId;
	private String fromCurrency;
	private String toCurrency;
	private ZonedDateTime fromDate;
	private ZonedDateTime toDate;
	private BigDecimal conversionRate;
	private BigDecimal inverseRate;
	private String source;
	private Boolean enabledFlag;
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getFxRateId() {
		return fxRateId;
	}
	public void setFxRateId(String fxRateId) {
		this.fxRateId = fxRateId;
	}
	public String getFromCurrency() {
		return fromCurrency;
	}
	public void setFromCurrency(String fromCurrency) {
		this.fromCurrency = fromCurrency;
	}
	public String getToCurrency() {
		return toCurrency;
	}
	public void setToCurrency(String toCurrency) {
		this.toCurrency = toCurrency;
	}
	public ZonedDateTime getFromDate() {
		return fromDate;
	}
	public void setFromDate(ZonedDateTime fromDate) {
		this.fromDate = fromDate;
	}
	public ZonedDateTime getToDate() {
		return toDate;
	}
	public void setToDate(ZonedDateTime toDate) {
		this.toDate = toDate;
	}
	public BigDecimal getConversionRate() {
		return conversionRate;
	}
	public void setConversionRate(BigDecimal conversionRate) {
		this.conversionRate = conversionRate;
	}
	public BigDecimal getInverseRate() {
		return inverseRate;
	}
	public void setInverseRate(BigDecimal inverseRate) {
		this.inverseRate = inverseRate;
	}
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public Boolean getEnabledFlag() {
		return enabledFlag;
	}
	public void setEnabledFlag(Boolean enabledFlag) {
		this.enabledFlag = enabledFlag;
	}
	
}
