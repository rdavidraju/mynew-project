package com.nspl.app.web.rest.dto;

public class RowConditionsDTO {
	private String ftlService;
	private String columnName;
	private Long templateLineId;
	private String operator;
	private String operatorMeaning;
	private String value;
	private String type;
	private String logicalOperator;
	private String logicalOperatorMeaning;
	
	public String getColumnName() {
		return columnName;
	}
	public void setColumnName(String columnName) {
		this.columnName = columnName;
	}
	public Long getTemplateLineId() {
		return templateLineId;
	}
	public void setTemplateLineId(Long templateLineId) {
		this.templateLineId = templateLineId;
	}
	public String getOperator() {
		return operator;
	}
	public void setOperator(String operator) {
		this.operator = operator;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getLogicalOperator() {
		return logicalOperator;
	}
	public void setLogicalOperator(String logicalOperator) {
		this.logicalOperator = logicalOperator;
	}
	public String getOperatorMeaning() {
		return operatorMeaning;
	}
	public void setOperatorMeaning(String operatorMeaning) {
		this.operatorMeaning = operatorMeaning;
	}
	public String getLogicalOperatorMeaning() {
		return logicalOperatorMeaning;
	}
	public void setLogicalOperatorMeaning(String logicalOperatorMeaning) {
		this.logicalOperatorMeaning = logicalOperatorMeaning;
	}
	public String getFtlService() {
		return ftlService;
	}
	public void setFtlService(String ftlService) {
		this.ftlService = ftlService;
	}
	
	
}
