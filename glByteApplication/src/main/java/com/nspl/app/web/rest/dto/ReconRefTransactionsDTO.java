package com.nspl.app.web.rest.dto;

public class ReconRefTransactionsDTO {
	
	private String reconReference;
	private String groupId;
	private String viewId;
	private String sourceOrTarget;
	
	public String getReconReference() {
		return reconReference;
	}
	public void setReconReference(String reconReference) {
		this.reconReference = reconReference;
	}
	public String getGroupId() {
		return groupId;
	}
	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}
	public String getViewId() {
		return viewId;
	}
	public void setViewId(String viewId) {
		this.viewId = viewId;
	}
	public String getSourceOrTarget() {
		return sourceOrTarget;
	}
	public void setSourceOrTarget(String sourceOrTarget) {
		this.sourceOrTarget = sourceOrTarget;
	}
	
}
