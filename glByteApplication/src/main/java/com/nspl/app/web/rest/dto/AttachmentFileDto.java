package com.nspl.app.web.rest.dto;

import java.util.List;

import com.nspl.app.domain.AttachmentFiles;

public class AttachmentFileDto {
	
	private Long id;
	private String attachmentKey;
	private String attachmentCategory;
	private List<AttachmentFiles> attachmentFilesList;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getAttachmentKey() {
		return attachmentKey;
	}
	public void setAttachmentKey(String attachmentKey) {
		this.attachmentKey = attachmentKey;
	}
	public String getAttachmentCategory() {
		return attachmentCategory;
	}
	public void setAttachmentCategory(String attachmentCategory) {
		this.attachmentCategory = attachmentCategory;
	}
	public List<AttachmentFiles> getAttachmentFilesList() {
		return attachmentFilesList;
	}
	public void setAttachmentFilesList(List<AttachmentFiles> attachmentFilesList) {
		this.attachmentFilesList = attachmentFilesList;
	}
	
	

}
